// kilometerToMeter

function kilometerToMeter(distance) {
    if (distance > 0) {
        convertedDistance = distance * 1000;
        return convertedDistance;
    }
    else if(distance <= 0) {
        console.log("Distance can not be negative/zero.");
    }
    else {
        console.log("Enter a value to convert to meter.");
    }
}


